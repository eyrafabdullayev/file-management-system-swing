    private void btnDeleteItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteItemActionPerformed
        int getSelectedRowId = tblNonAccessibleFolders.getSelectedRow();
        int id = (Integer) tblNonAccessibleFolders.getModel().getValueAt(getSelectedRowId, 0);
        
        String folderName = nonAccessibleFolders.get(id-1);
        int userId = selectedUser.getId();
        
        if(fdb.deleteNonAccessibleFolders(userId, folderName)){
            JOptionPane.showMessageDialog(this, "Table changed!");
            cbUsers.setSelectedIndex(getSelectedIndexCB);
        }else{
            JOptionPane.showMessageDialog(this, "There is a problem!");
        }
    }//GEN-LAST:event_btnDeleteItemActionPerformed

