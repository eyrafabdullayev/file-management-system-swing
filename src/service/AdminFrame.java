    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed
      String pathName = txtPathName.getText();
      if(fdb.addNonAccessibleFolders(pathName, selectedUser.getId())){
          JOptionPane.showMessageDialog(this, "Table changed!");
          txtPathName.setText("");
          cbUsers.setSelectedIndex(getSelectedIndexCB); // refresh nonAccessibleFoldersList and combo box selected item
      }else{
          JOptionPane.showMessageDialog(this, "There is a problem!");
      }
    }//GEN-LAST:event_btnAddItemActionPerformed

