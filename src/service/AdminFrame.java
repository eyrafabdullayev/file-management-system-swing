    private void btnActiveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActiveActionPerformed
        int getSelectedRowId = tblUsers.getSelectedRow();

        if (getSelectedRowId != -1) {
            int id = (Integer) tblUsers.getModel().getValueAt(getSelectedRowId, 0);

            if (udb.makeActive(id)) {
                refresh();
                JOptionPane.showMessageDialog(this, "Table successfully changed!");
            } else {
                JOptionPane.showMessageDialog(this, "There is a problem!");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Please select item from the table!");
        }
    }//GEN-LAST:event_btnActiveActionPerformed

