    public void downloadAllUsers() {
        allUsers = udb.getAllUsers();
    }

    public void refresh() {
        usersPanel.setVisible(true);
        setNonAccessibleFolders.setVisible(false);

        downloadAllUsers();

        Object[] columnNames = Arrays.asList("ID", "Name", "Surname", "Username", "Password", "Status", "Position").toArray();
        Object[][] data = new Object[allUsers.size()][columnNames.length]; //-1 for admin accunt

        for (int i = 0; i < allUsers.size(); i++) {
            User u = allUsers.get(i);
            data[i] = Arrays.asList(u.getId(), u.getName(), u.getSurname(), u.getUsername(), u.getPassword(), u.getStatus(), u.getPosition()).toArray();
        }

        DefaultTableModel dtm = new DefaultTableModel(data, columnNames);
        tblUsers.setModel(dtm);
    }

    public void fillAndUseComboBox() {
        JComboBox jcb = cbUsers;
        jcb.removeAllItems();

        for (int i = 0; i < allUsers.size(); i++) {
            User u = allUsers.get(i);
            jcb.addItem(u.getName());
        }
        
    }

    public void setNonAccessibleFolders() {
        setNonAccessibleFolders.setVisible(true);
        usersPanel.setVisible(false);

        downloadAllUsers();
        fillAndUseComboBox(); // fill and use jcombo box
        
        cbUsers.addActionListener((ActionEvent evt) -> { //selected
            int id = myBox(evt);
            
            FileDB fdb = new FileDB();
            nonAccessibleFolders = fdb.getNonAccessibleFolders(id);
            
            Object[] columnNames = Arrays.asList("ID","Name").toArray();
            Object[][] data = new Object[nonAccessibleFolders.size()][columnNames.length];
            
            for(int i=0;i<nonAccessibleFolders.size();i++){
                String folder = nonAccessibleFolders.get(i);
                data[i] = Arrays.asList(i+1,folder).toArray();
            }
            
            DefaultTableModel dtm = new DefaultTableModel(data,columnNames);
            tblNonAccessibleFolders.setModel(dtm);
        });
    }

    protected int myBox(ActionEvent evt) {
        if (cbUsers.getSelectedItem() != null) {
            String name = cbUsers.getSelectedItem().toString();
            
            for(int i=0;i<allUsers.size();i++){
                User u = allUsers.get(i);
                if(u.getName().equals(name)){
                    selectedUser = u;
                    getSelectedIndexCB = cbUsers.getSelectedIndex();
                    return u.getId();
                }
            }
        }
        
        return 9;
    }

